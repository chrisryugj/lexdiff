// 도+시/군/구 조합 (예: "경기 수원시", "서울 강남구")
const REGION_PATTERN = /((?:서울특별시|서울시|서울|부산광역시|부산시|부산|대구광역시|대구시|대구|인천광역시|인천시|인천|광주광역시|광주시|광주|대전광역시|대전시|대전|울산광역시|울산시|울산|세종특별자치시|세종시|세종|경기도|경기|강원특별자치도|강원도|강원|충청북도|충북|충청남도|충남|전라북도|전북특별자치도|전북|전라남도|전남|경상북도|경북|경상남도|경남|제주특별자치도|제주도|제주)\s*)?([가-힣]+[시군구])/

// 독립 도시/도 이름만 (시/군/구 없이도 매칭, REGION_PATTERN 실패 시 fallback)
const STANDALONE_REGION_PATTERN = /^(서울특별시|서울시|서울|부산광역시|부산시|부산|대구광역시|대구시|대구|인천광역시|인천시|인천|광주광역시|광주시|광주|대전광역시|대전시|대전|울산광역시|울산시|울산|세종특별자치시|세종시|세종|경기도|경기|강원특별자치도|강원도|강원|충청북도|충북|충청남도|충남|전라북도|전북특별자치도|전북|전라남도|전남|경상북도|경북|경상남도|경남|제주특별자치도|제주도|제주)\s/

// 복합어 분해용 — 사전에 등록된 모든 키를 길이 내림차순으로 정렬
// "중소기업육성기금" → "중소기업" + "육성" + "기금" 을 각각 찾기 위해
const allKnownTerms: string[] = []
const knownTermSet = new Set<string>()

// 모든 사전에서 키 수집
for (const key of synonymIndex.keys()) knownTermSet.add(key)
for (const key of Object.keys(HYPERNYM_MAP)) knownTermSet.add(key.toLowerCase())
for (const key of Object.keys(COLLOQUIAL_TO_LEGAL)) knownTermSet.add(key.toLowerCase())
for (const key of Object.keys(TOPIC_CLUSTERS)) knownTermSet.add(key.toLowerCase())

// 긴 용어부터 먼저 매칭 (greedy)
for (const term of knownTermSet) {
  if (term.length >= 2) allKnownTerms.push(term)
}
allKnownTerms.sort((a, b) => b.length - a.length)

/**
 * 복합어를 사전에 등록된 하위 용어로 분해
 * "중소기업육성기금" → ["중소기업", "기금"]
 * "소상공인창업지원" → ["소상공인", "창업"]
 */
function decomposeCompound(word: string): string[] {
  const lower = word.toLowerCase()
  const found: string[] = []
  let remaining = lower

  for (const term of allKnownTerms) {
    if (remaining.includes(term)) {
      found.push(term)
      remaining = remaining.replace(term, ' ')
    }
  }

  return found
}

