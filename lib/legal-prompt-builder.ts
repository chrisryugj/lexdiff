/**
 * 질문 유형에 맞는 시스템 프롬프트 생성
 */
export function buildLegalPrompt(queryType: LegalQueryType): string {
  const typeTemplate = TYPE_TEMPLATES[queryType] || TYPE_TEMPLATES.application

  return `${COMMON_SYSTEM_PROMPT}

${typeTemplate}`
}

/**
 * 기존 file-search-client.ts 호환용
 * queryType을 기존 4가지 타입으로 매핑
 */
export function buildPromptForLegacyType(legacyType: string): string {
  const typeMapping: Record<string, LegalQueryType> = {
    'specific': 'definition',
    'general': 'application',
    'comparison': 'comparison',
    'procedural': 'procedure'
  }

  const queryType = typeMapping[legacyType] || 'application'
  return buildLegalPrompt(queryType)
}

/**
 * 모든 프롬프트 템플릿 반환 (디버깅/테스트용)
 */
export function getAllPromptTemplates(): Record<LegalQueryType, string> {
  const result: Record<string, string> = {}

  for (const type of Object.keys(TYPE_TEMPLATES) as LegalQueryType[]) {
    result[type] = buildLegalPrompt(type)
  }

  return result as Record<LegalQueryType, string>
}
